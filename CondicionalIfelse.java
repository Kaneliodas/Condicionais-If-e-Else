import java.util.*;

public class CondicionalIfelse {
    public static void main(String[] args) {
      int nota = 62;
      if (nota >= 90) {
      System.out.println("Conceito A");
      }
      else if (nota >= 80) {
      System.out.println("Conceito B");
      }
      else if (nota >= 70) {
      System.out.println("Conceito C");
      }
      else {
      System.out.println("Conceito D");
      }
  }
}
